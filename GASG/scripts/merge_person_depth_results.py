"""Merge PersonDataset depth model JSON files into paper tables and plots."""

from __future__ import annotations

import argparse
import csv
import glob
import json
import os
from typing import Dict, List, Tuple

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np


ORDER = [
    "defom_identity",
    "defom_shift",
    "defom_dav2_warp",
    "defom_moge_warp",
    "defom_zerostereo_tuned",
    "defom_stereospace",
    "gasg_defom",
    "defom_real",
]

METRICS: List[Tuple[str, str, bool, str]] = [
    ("abs_rel", "AbsRel", False, ".4f"),
    ("sq_rel", "SqRel", False, ".4f"),
    ("rmse", "RMSE", False, ".4f"),
    ("rmse_log", "RMSElog", False, ".4f"),
    ("d1", "d1", True, ".4f"),
    ("d2", "d2", True, ".4f"),
    ("d3", "d3", True, ".4f"),
    ("inference_time_ms_mean", "Time (ms)", False, ".0f"),
]


def load_rows(results_dir: str) -> Dict[str, Dict]:
    rows = {}
    for path in sorted(glob.glob(os.path.join(results_dir, "*.json"))):
        if os.path.basename(path).startswith("summary"):
            continue
        with open(path, "r", encoding="utf-8") as f:
            row = json.load(f)
        rows[row["method"]] = row
    return rows


def ordered_rows(rows: Dict[str, Dict]) -> List[Dict]:
    return [rows[m] for m in ORDER if m in rows]


def best_indices(rows: List[Dict], key: str, higher: bool) -> Tuple[int, int]:
    valid = [(i, r) for i, r in enumerate(rows) if key in r]
    if len(valid) < 2:
        return -1, -1
    valid.sort(key=lambda x: x[1][key], reverse=higher)
    return valid[0][0], valid[1][0]


def fmt_tex(value: float, spec: str, best: bool, second: bool) -> str:
    s = format(value, spec)
    if best:
        return "\\textbf{" + s + "}"
    if second:
        return "\\underline{" + s + "}"
    return s


def write_tables(rows: List[Dict], out_dir: str) -> None:
    os.makedirs(out_dir, exist_ok=True)
    csv_path = os.path.join(out_dir, "summary_depth_models.csv")
    with open(csv_path, "w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["method", "type", "scale"] + [m[1] for m in METRICS])
        for r in rows:
            writer.writerow([r["pretty"], r["type"], r["scale_alignment"]] +
                            [r.get(m[0], "") for m in METRICS])

    md = [
        "# PersonDataset depth model comparison",
        "",
    ]
    if rows:
        md += [
            f"- Evaluated samples: **{max(int(r.get('n_eval', 0)) for r in rows)}**",
            f"- Image resolution: {rows[0].get('height', 256)} x {rows[0].get('width', 576)}",
            f"- Device: {rows[0].get('device', 'cuda')}",
            "",
        ]
    md.append("| Method | Type | Scale | AbsRel ↓ | SqRel ↓ | RMSE ↓ | RMSElog ↓ | d1 ↑ | d2 ↑ | d3 ↑ | Time ms ↓ |")
    md.append("|---|---|---|---:|---:|---:|---:|---:|---:|---:|---:|")
    for r in rows:
        md.append(
            f"| {r['pretty']} | {r['type']} | {r['scale_alignment']} | "
            f"{r['abs_rel']:.4f} | {r['sq_rel']:.4f} | {r['rmse']:.4f} | "
            f"{r['rmse_log']:.4f} | {r['d1']:.4f} | {r['d2']:.4f} | "
            f"{r['d3']:.4f} | {r['inference_time_ms_mean']:.0f} |"
        )
    with open(os.path.join(out_dir, "summary_depth_models.md"),
              "w", encoding="utf-8") as f:
        f.write("\n".join(md) + "\n")

    best = {k: best_indices(rows, k, higher) for k, _, higher, _ in METRICS}
    n_eval = max((int(r.get("n_eval", 0)) for r in rows), default=0)
    height = rows[0].get("height", 256) if rows else 256
    width = rows[0].get("width", 576) if rows else 576
    device = rows[0].get("device", "cuda") if rows else "cuda"

    tex = [
        "% =============================================================",
        "% Table: PersonDataset depth model comparison",
        f"%   N = {n_eval}, {device}, {height} x {width}.",
        "%   Auto-generated by scripts/merge_person_depth_results.py",
        "% =============================================================",
        "\\begin{table}[t]",
        "\\centering",
        "\\caption{Pseudo-stereo depth estimation comparison on PersonDataset "
        f"(N={n_eval}, {height}$\\times${width}, {device}). "
        "All methods use DEFOM-Stereo as the downstream stereo estimator; "
        "the compared factor is the source of the right view.}",
        "\\label{tab:person_depth_models}",
        "\\resizebox{\\linewidth}{!}{",
        "\\begin{tabular}{l c c r r r r r r r r}",
        "\\toprule",
        "Method & Type & Scale & AbsRel$\\downarrow$ & SqRel$\\downarrow$ & "
        "RMSE$\\downarrow$ & RMSElog$\\downarrow$ & $\\delta_1\\uparrow$ & "
        "$\\delta_2\\uparrow$ & $\\delta_3\\uparrow$ & Time (ms)$\\downarrow$ \\\\",
        "\\midrule",
    ]
    for i, r in enumerate(rows):
        vals = []
        for key, _, _, spec in METRICS:
            vals.append(fmt_tex(
                r[key], spec, i == best[key][0], i == best[key][1]))
        tex.append(
            f"{r['pretty']} & {r['type']} & {r['scale_alignment']} & "
            + " & ".join(vals) + " \\\\"
        )
        if r["method"] == "da3_median":
            tex.append("\\midrule")
        if r["method"] == "defom_zerostereo_tuned":
            tex.append("\\midrule")
    tex += [
        "\\bottomrule",
        "\\end{tabular}}",
        "\\end{table}",
    ]
    with open(os.path.join(out_dir, "table_depth_models.tex"),
              "w", encoding="utf-8") as f:
        f.write("\n".join(tex) + "\n")

    with open(os.path.join(out_dir, "summary_depth_models.json"),
              "w", encoding="utf-8") as f:
        json.dump(rows, f, indent=2)


def plot_bars(rows: List[Dict], out_dir: str) -> None:
    fig_dir = os.path.join(out_dir, "figures")
    os.makedirs(fig_dir, exist_ok=True)
    if not rows:
        return
    short_names = {
        "defom_identity": "DEFOM copy",
        "defom_shift": "DEFOM shift",
        "defom_dav2_warp": "DA-V2 warp",
        "defom_moge_warp": "MoGe warp",
        "defom_zerostereo": "ZeroStereo",
        "defom_zerostereo_tuned": "ZeroStereo tuned",
        "defom_stereospace": "StereoSpace",
        "gasg_defom": "GASG",
        "defom_real": "Real-right DEFOM",
    }
    names = [short_names.get(r["method"], r["pretty"]) for r in rows]
    y = np.arange(len(rows))

    fig, axes = plt.subplots(1, 3, figsize=(11.2, 5.0), dpi=240,
                             sharey=True)
    specs = [
        ("abs_rel", "AbsRel lower is better", "#4c78a8"),
        ("rmse", "RMSE lower is better", "#f58518"),
        ("d1", "delta1 higher is better", "#54a24b"),
    ]
    for ax, (key, title, color) in zip(axes, specs):
        vals = [r[key] for r in rows]
        ax.barh(y, vals, color=color, height=0.68)
        ax.set_title(title)
        ax.set_yticks(y)
        if ax is axes[0]:
            ax.set_yticklabels(names, fontsize=7.5)
        else:
            ax.tick_params(axis="y", labelleft=False)
        ax.tick_params(axis="x", labelsize=7)
        ax.grid(axis="x", alpha=0.25)
        ax.spines[["top", "right"]].set_visible(False)
        ax.set_xlim(0.0, max(vals) * 1.18)
        pad = max(vals) * 0.018
        for i, v in enumerate(vals):
            ax.text(v + pad, i, f"{v:.3f}", ha="left", va="center",
                    fontsize=6.5)
    axes[0].invert_yaxis()
    fig.tight_layout(w_pad=1.0)
    fig.savefig(os.path.join(fig_dir, "depth_metrics_bars.png"), dpi=240)
    plt.close(fig)

    fig, ax = plt.subplots(figsize=(7.2, 5.0), dpi=240)
    times = [r["inference_time_ms_mean"] for r in rows]
    ax.barh(y, times, color="#b279a2", height=0.68)
    ax.set_xscale("log")
    ax.set_title("Inference time per image, log scale")
    ax.set_xlabel("ms")
    ax.set_yticks(y)
    ax.set_yticklabels(names, fontsize=7.5)
    ax.grid(axis="x", alpha=0.25)
    ax.spines[["top", "right"]].set_visible(False)
    ax.invert_yaxis()
    fig.tight_layout()
    fig.savefig(os.path.join(fig_dir, "depth_runtime_bars.png"), dpi=240)
    plt.close(fig)


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--results_dir", default="results/person_depth_models")
    args = p.parse_args()
    rows = ordered_rows(load_rows(args.results_dir))
    write_tables(rows, args.results_dir)
    plot_bars(rows, args.results_dir)
    print(f"Merged {len(rows)} depth result files in {args.results_dir}")


if __name__ == "__main__":
    main()
